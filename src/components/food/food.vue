			<div class='back' @click='hide'>
				<i class='icon-arrow_lift'></i>
			</div>
		</div>
		<div class='content'>
			<h1 class='title' v-text='food.name'></h1>
			<div class='detail'>
				<span class='sell-count' v-text="'月售' + food.sellCount + '份'"></span>
				<span class='rating' v-text="'好评率' + food.rating + '%'"></span>
			</div>
			<div class='price'>
				<span class='now' v-text="'￥'+food.price"></span>
				<span class='old' v-show='food.oldPrice' v-text="'￥'+food.oldPrice"></span>
			</div>
			<div class='cartcontrol-wrapper'>
				<cartcontrol :food='food'></cartcontrol>
			</div>
			<div v-show='!food.count || food.count===0' class='buy' @click.stop.prevent='addFirst($event)' transition='fade'>加入购物车</div>
		</div>
		<split v-show="food.info"></split>
		<div class="info" v-show="food.info">
			<h1 class="title">商品信息</h1>
			<p class="text" v-text="food.info"></p>
		</div>
		<split></split>
		<div class="rating">
			<h1 class="title">商品评价</h1>
			<ratingselect :ratings="food.ratings" :select-type="selectType" :only-content="onlyContent" :desc="desc"></ratingselect>
		</div>
		<div class="rating-wrapper">
			<ul v-show="food.ratings && food.ratings.length">
				<li v-show="needShow(rating.rateType, rating.text)" v-for="rating in food.ratings" class="rating-item border-1px">
					<div class="user">
						<span class="name" v-text="rating.username"></span>
						<img class="avatar" width="12" height="12" :src="rating.avatar">
					</div>
					<div class="time" v-text="rating.rateTime | formatDate"></div>
					<p class="text">
						<span :class="{'icon-thumb_up': rating.rateType===0, 'icon-thumb_down': rating.rateType===1}"></span><span v-text="rating.text"></span>
					</p>
				</li>
			</ul>
			<div class="no-rating" v-show="!(food.ratings && food.ratings.length)">暂无评价</div>
		</div>
	</div>
</div>
</template>

<script type="text/ecmascript-6">
import Vue from 'vue';
import BScroll from 'better-scroll';
import cartcontrol from 'components/cartcontrol/cartcontrol';
import split from 'components/split/split';
import ratingselect from 'components/ratingselect/ratingselect';
import {formatDate} from 'common/js/date';

// const POSITIVE = 0;
// const NEGATIVE = 1;
const ALL = 2;

export default {
	name: 'food',
	props: {
		food: {
			type: Object
		}
	},
	data() {
		return {
			showFlag: false,
			scroll: null,
			// for ratingselect.vue
			selectType: ALL,
			onlyContent: true,
			desc: {
				all: '全部',
				positive: '推荐',
				negative: '吐槽'
			}
		};
	},
	methods: {
		show() {
			this.showFlag = true;
			this.selectType = ALL;
			this.onlyContent = true;
			this.$nextTick(() => {
				if (this.scroll) {
					this.scroll.refresh();
				} else {
					this.scroll = new BScroll(this.$els['food'], {
						click: true
					});
				}
			});
		},
		hide() {
			this.showFlag = false;
		},
		addFirst(event) {
			if (!event._constructed) {  // 解决pc端点击出现两次bug,_constructed是better-scroll自带的,原生不存在
				return;
			}
			Vue.set(this.food, 'count', 1);
			this.$dispatch('cart.add', event.target);
		},
		needShow(type, text) {
			if (this.onlyContent && !text) {
				return false;
			}
			if (this.selectType === ALL) {
				return true;
			} else {
				return this.selectType === type;
			}
		}
	},
	filters: {
		formatDate(time) {
			let date = new Date(time);
			return formatDate(date, 'yyyy-MM-dd hh:mm');
		}
	},
	events: {
		'ratingselect.type'(type) {
			this.selectType = type;
			this.$nextTick(() => {
				this.scroll.refresh();
			});
		},
		'ratingselect.toggle'(onlyContent) {
			this.onlyContent = onlyContent;
			this.$nextTick(() => {
				this.scroll.refresh();
			});
		}
	},
	components: {
		cartcontrol,
		split,
		ratingselect
	}
};
</script>

